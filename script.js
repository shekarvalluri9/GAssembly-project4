function checkanswer(answer) {
    var correctanswer = "Australia";
    var feedbackElement = document.getElementById('feedback');
    if (answer == correctanswer) {
        feedbackElement.innerHTML = "Yeh!, that's correct answer! well done mate";
    }
    else{
        feedbackElement.innerHTML = "No that's incorrect you may want to check again your answer!";
    }
}
